  # Helper to render theme toggle with appropriate icons
  def theme_toggle_button(options = {})
    button_classes = options[:class] || "p-2 text-gray-500 dark:text-gray-400 hover:text-primary-600 dark:hover:text-primary-400 rounded-full focus:outline-none transition-transform hover:scale-110"

    content_tag :button,
                type: "button",
                class: button_classes,
                data: {
                  controller: "theme",
                  action: "click->theme#toggle"
                },
                aria: {
                  label: "Toggle theme"
                } do
      concat icon("sun", class: "h-6 w-6 dark:block hidden", data: { theme_target: "lightIcon" })
      concat icon("moon", class: "h-6 w-6 dark:hidden block", data: { theme_target: "darkIcon" })
    end
  end

